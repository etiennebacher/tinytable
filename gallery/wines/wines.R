# helper functions
slugify <- function(x) {
  x <- gsub("[^[:alnum:] ]", "", x)
  x <- gsub(" ", "_", x)
  x <- tolower(x)
  return(x)
}

side_by_side_images <- function(file_paths, height, margin = "1px") {
  if (length(file_paths) == 0 || all(is.na(file_paths))) {
    return("")
  }
  file_paths <- file_paths[!is.na(file_paths)]
  img_tags <- paste0('<img src="', file_paths, '" height="', height, '" style="margin:', margin, ';"/>', collapse = "")
  return(img_tags)
}


