# Wine Table Generator using tinytable
#
# This script demonstrates advanced tinytable features to create a visually rich wine table.
# It showcases plot_tt() for embedding images, raw HTML in data cells, and custom styling.
#
# tinytable Column Implementation:
# 1. Bottle Image (empty header): Uses plot_tt() to insert bottle PNG images from local files into data cells
# 2. Wine Name: Plain text data with vintage years extracted and moved to domain_year column
# 3. Domain & Year: Raw HTML with <br> tags embedded in data cells for line breaks
# 4. Region: Raw HTML combining Unicode flag symbols and region text with CSS styling
# 5. Grapes: Raw HTML generated by draw_grapes() function, embedding grape PNG images via <img> tags
# 6. Food Pairings: Raw HTML generated by draw_pairings() function, embedding pairing SVG icons via <img> tags
# 7. Rating: Raw HTML with Unicode stars (⭐), CSS styling, and <br> tags for multi-line display
# 8. Price ($): Numeric data formatted using format_tt() with scales::label_currency()
# 9. Alcohol Content (%): Numeric data formatted using format_tt() with scales::label_percent()
#
# Key tinytable features used:
# - plot_tt(): Inserts column header SVG icons and bottle images into table cells
# - format_tt(): Applies currency and percentage formatting to numeric columns
# - style_tt(): Controls alignment, colors, fonts, and table borders
# - Raw HTML in data cells: Enables custom styling and embedded images
#
# The code used to create this table was adapted to use the [`tinytable`](https://vincentarelbundock.github.io/tinytable/) package in R, based on code originally written by [Abdoul Madjid](https://www.abdoulblog.com) ([GitHub](https://github.com/AbdoulMa)) as a [submission to the RStudio Table Contest 2021](https://posit.co/blog/rstudio-table-contest-2021/), with the original code available on [GitHub](https://github.com/AbdoulMa/RStudio-Table-Contest-2021). The table uses data on some of the world’s most prestigious wines from [Vivino](https://www.vivino.com/), including names, vintages, grape varieties, regions, prices, ratings, allergens, and food pairings, along with the original icons and visual elements.


library(tinytable)
